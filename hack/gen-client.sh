set -euo pipefail

ROOT="$(git rev-parse --show-toplevel)"

if [ ! -d "$ROOT" ]; then
  echo "Unable to find ${ROOT} folder"
  exit 1
fi

# TODO(b/479141098): The part below - installing controller-gen manually
# and pinning a specific code-generator version is temporary and may blow up
# in the future. This is currently necessary because of g3 version of kubernetes
# not supporting the code generated by more up-to-date versions leading to
# issues when exporting the client for e2e tests via copybara.

# Ensure controller-tools are installed with the go.mod version.
CT_MODULE="sigs.k8s.io/controller-tools"
CT_VERSION=$(go list -m -f '{{.Version}}' "${CT_MODULE}")
if [ -z "${CT_VERSION}" ]; then
  echo >&2 "ERROR: ${CT_MODULE} not found in go.mod"
  exit 1
fi
echo "INFO: Ensuring controller-gen ${CT_VERSION} is installed..."
go install "${CT_MODULE}/cmd/controller-gen@${CT_VERSION}"

# Specify the desired version of code-generator
CODEGEN_PKG="k8s.io/code-generator@v0.30.3"
echo "INFO: Using ${CODEGEN_PKG}"
# Get the path to the specified version in the module cache
CODEGEN_PATH=$(go list -m -f '{{.Dir}}' "${CODEGEN_PKG}")

if [ -z "${CODEGEN_PATH}" ]; then
  echo >&2 "Failed to find module ${CODEGEN_PKG}. Running 'go mod download'."
  go mod download "${CODEGEN_PKG}"
  CODEGEN_PATH=$(go list -m -f '{{.Dir}}' "${CODEGEN_PKG}")
  if [ -z "${CODEGEN_PATH}" ]; then
    echo >&2 "Failed to download and find module ${CODEGEN_PKG}"
    exit 1
  fi
fi

. "${CODEGEN_PATH}/kube_codegen.sh"

# Generate client
BOILERPLATE_FILE="${ROOT}/hack/boilerplate.go.txt"
API_PATH="${ROOT}/api"
CLIENT_PATH="${ROOT}/client"

OUTPUT_PKG="github.com/googlecloudplatform/compute-class-api"
OUTPUT_CLIENT_PKG="${OUTPUT_PKG}/client"

echo "INFO: Cleaning client directory..."
rm -rf "${CLIENT_PATH}"

echo "INFO: Generating helpers..."
GOTOOLCHAIN=go1.24.0 kube::codegen::gen_helpers "${API_PATH}" --boilerplate "${BOILERPLATE_FILE}"

echo "INFO: Generating clients..."
GOTOOLCHAIN=go1.24.0 kube::codegen::gen_client "${API_PATH}" \
    --output-dir "${CLIENT_PATH}" \
    --output-pkg "${OUTPUT_CLIENT_PKG}" \
    --with-applyconfig \
    --with-watch \
    --boilerplate "${BOILERPLATE_FILE}"

echo "INFO: Generating CRD manifest..."
GOTOOLCHAIN=go1.24.0 go tool controller-gen \
  crd:generateEmbeddedObjectMeta=true \
  paths="${API_PATH}/..." \
  output:crd:dir="${ROOT}"

echo "INFO: Running go mod tidy..."
go mod tidy
